def generateMyNoiseSeq(notes):
  Tk = 8 # s, for each note
  Tf = Tk # s, time fading in and out

  notes = np.array(notes)
  s = 2 * generateSineSeq(notes=notes, Tk=Tk, Tf=Tf)
  s += 1 * generateSineSeq(notes=12+notes, Tk=Tk, Tf=Tf)
  # s += 0.5 * generateSineSeq(notes=24+notes, Tk=Tk, Tf=Tf)

  return s

def songMyNoiseNet1Major():
  s = generateMyNoiseSeq(notes=[13,9,4,11])
  writeWav(s, "song.mynoise.net.1.major.wav")

def songMyNoiseNet1Minor():
  s = generateMyNoiseSeq(notes=[16,12,7,14])
  writeWav(s, "song.mynoise.net.1.minor.wav")

def songMyNoiseNet2Minor():
  s = generateMyNoiseSeq(notes=[16,14,12,14])
  writeWav(s, "song.mynoise.net.2.minor.wav")

